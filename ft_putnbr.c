#include "libft.h"

void	ft_putnbr(int n)
{
	long int	nb;
	long int	i;

	nb = n;
	i = 1;
	if (nb < 0)
	{
		ft_putchar('-');
		nb *= -1;
	}
	while (nb / i > 9)
		i *= 10;
	while (i > 0)
	{
		ft_putchar(nb / i + '0');
		nb %= i;
		i /= 10;
	}
}
